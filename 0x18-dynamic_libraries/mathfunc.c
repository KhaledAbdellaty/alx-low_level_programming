#include "math.h"
int add(int a, int b)
{
	int sum = a + b;
	return (sum);
}
int sub(int a, int b)
{
        int res = a - b;
        return (res);
}
int mul(int a, int b)
{
        int res = a * b;
        return (res);
}
int div(int a, int b)
{
        int res = a / b;
        return (res);
}
int mod(int a, int b)
{
        int res = a % b;
        return (res);
}
